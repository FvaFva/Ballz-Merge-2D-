public struct SavedBlock
{
    public int ID;
    public int Number;
    public int GridPositionX;
    public int GridPositionY;

    public SavedBlock(int iD, int number, int gridPositionX, int gridPositionY)
    {
        ID = iD;
        Number = number;
        GridPositionX = gridPositionX;
        GridPositionY = gridPositionY;
    }
}
